import {float} from "../interfaces/float";

import {C as CAlias} from "./C";
const C = CAlias;

import {Basic as BasicAlias} from "../basicFunctions/Basic";
const Basic = BasicAlias;

import {Conversion as ConversionAlias} from "../core/Conversion";
const Conversion = ConversionAlias;

import {Root as RootAlias} from "../basicFunctions/Root";
const Root = RootAlias;

import {Log as LogAlias} from "../basicFunctions/Log";
const Log = LogAlias;

import {P as PAlias} from "../core/P";
const P = PAlias;
export type P = PAlias;


type Entry = {
  sqrt: float,
  cbrt: float,
  fourthRoot: float,
  truncLogEPSDiv2: float,
  reciprocalSqrt: float,
  oneMinusEPS: float
};


export class EPSILON {
  private static _table: {[numDigits: number]: Entry};

  private static createEntry(prec: P): Entry {
    const sqrtEps = Root.squareF(prec.epsilon, prec);
    const entry: Entry = {
      sqrt: sqrtEps,
      cbrt: Root.fn(prec.epsilon, 3, prec),
      fourthRoot: Root.fn(prec.epsilon, 4, prec),
      truncLogEPSDiv2: Conversion.trunc(Basic.divideFF(
        Log.f(prec.epsilon, prec),
        C.F_NEG_2,
        prec
      )),
      reciprocalSqrt: Basic.reciprocalF(sqrtEps, prec),
      oneMinusEPS: Basic.subtractFF(C.F_1, prec.epsilon, prec)
    };
    EPSILON._table[prec.numDigits] = entry;

    return entry;
  }

  private static getEntry(prec: P): Entry {
    if (typeof EPSILON._table === "undefined") { EPSILON._table = {}; }

    let entry: Entry = EPSILON._table[prec.numDigits];

    if (typeof entry === "undefined") { entry = EPSILON.createEntry(prec);}

    return entry;
  }

  public static sqrt(prec: P): float { return EPSILON.getEntry(prec).sqrt; }

  public static cbrt(prec: P): float { return EPSILON.getEntry(prec).cbrt; }

  public static fourthRoot(prec: P): float { return EPSILON.getEntry(prec).fourthRoot; }

  public static truncLogEPSDiv2(prec: P): float {
    return EPSILON.getEntry(prec).truncLogEPSDiv2;
  }

  public static reciprocalSqrt(prec: P): float {
    return EPSILON.getEntry(prec).reciprocalSqrt;
  }

  public static oneMinusEPS(prec: P): float {
    return EPSILON.getEntry(prec).oneMinusEPS;
  }
}

